

num1=eval(input("enter a first number"))

num2=eval(input("enter a second number"))
operator=input("use operator (+,-,/,*)")
if operator=="+":
   result=num1+num2
   print(result)
elif operator=="-":
     result=num1-num2
     print(result)
elif operator=="/":
     result=num1/num2
     print(result)
elif operator=="*":
     result=num1*num2
     print(result)
